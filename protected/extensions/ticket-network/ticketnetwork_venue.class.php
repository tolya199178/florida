	/**
	 'WebsiteConfigID' => WEB_CONF_ID,
	 'venueID'
	 );
	
	 * @param string $params
	 * @return TNPerformer
	 */
	public function get($params=false){
	
		$this->_request['method'] = 'GetVenue';
	
		if($params){
			$this->set($params);
			if(array_key_exists('method', $params)){
				$this->_request['method'] = $params['method'];
				unset($params['method']);
			}
		}
	
		return $this;
	}
	
	/**
	 * Parse the results from TicketNetwork
	 * @return string|unknown
	 */
	public function parse(){
		$this->_data = array();
	
		try{
			parent::parse();
	
			if(!empty($this->_response)){
				if($this->_request['method'] == 'GetVenue'){
					if(is_array($this->_response->GetVenueResult->Venue)){
						foreach($this->_response->GetVenueResult->Venue as $venue){
							$this->_data[] = $venue;
						}
					}else{
						$this->_data = null;
					}
				}else if($this->_request['method'] == 'GetVenueConfigurations'){
					if(is_array($this->_response->GetVenueConfigurationsResult->VenueConfiguration)){
						foreach($this->_response->GetVenueConfigurationsResult->VenueConfiguration as $cfg){
							$this->_data[] = $cfg;
						}
					}else{
						$this->_data = $this->_response->GetVenueConfigurationsResult->VenueConfiguration;
					}
				}
			}
	
		}catch(TNException $e){
		}
	
		return $this;
	}
	
	/**
	 * Determines whether a seating chart exists for a venue
	 *
	 * @param string $which - key in options array to check value
	 * @param mixed $value - value of setting to check against, default checks for boolean TRUE
	 * @return boolean - true if the setting matches supplied value, false otherwise or array key missing
	 */
	public function chart($which){
	    $retval = false;
	
	    if(array_key_exists($which, $this->_map['charts']) && !empty($this->_map['charts'][$which])){
	        $retval = $this->_map['charts'][$which];
	    }
	
	    return $retval;
	}	
	
